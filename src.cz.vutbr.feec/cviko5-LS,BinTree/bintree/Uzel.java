package cz.vutbr.feec.dsa.bintree;

public class Uzel {
	private int hodnota;
	private Uzel pravy;
	private Uzel levy;
	public int getHodnota() {
		return hodnota;
	}
	public void setHodnota(int hodnota) {
		this.hodnota = hodnota;
	}
	public Uzel getPravy() {
		return pravy;
	}
	public void setPravy(Uzel pravy) {
		this.pravy = pravy;
	}
	public Uzel getLevy() {
		return levy;
	}
	public void setLevy(Uzel levy) {
		this.levy = levy;
	}
	
	
}
